# ── main ─────────────────────────────────────────────────────────────────────

def _run_r_script():
    r_script = Path(__file__).resolve().parent / "build_gallery_R.R"
    if not r_script.exists():
        print("  [R] build_gallery_R.R not found — skipping R figures")
        return False
    print("  [R] running build_gallery_R.R ...", end=" ", flush=True)
    try:
        result = subprocess.run(
            ["Rscript", str(r_script)],
            cwd=str(ROOT),
            capture_output=True, text=True, timeout=300,
        )
        if result.returncode == 0:
            print("ok")
            return True
        else:
            print(f"FAILED (exit {result.returncode})")
            if result.stderr:
                for line in result.stderr.strip().splitlines()[-5:]:
                    print(f"    {line}")
            return False
    except FileNotFoundError:
        print("Rscript not found — skipping R figures")
        return False


def main():
    print(f"Output: {OUT}")

    # Python figures
    for f in FIGURES:
        out_path = OUT / f"{f['key']}_py.png"
        print(f"  [py] {f['key']} ...", end=" ", flush=True)
        try:
            with warnings.catch_warnings():
                warnings.simplefilter("ignore", UserWarning)
                f["fn"](out_path)
            print(f"ok ({out_path.stat().st_size // 1024} KB)")
        except Exception as e:
            print(f"FAILED: {e}")
            raise

    # R figures
    _run_r_script()

    # Determine which R figures exist
    r_keys = {f["key"] for f in FIGURES if (OUT / f"{f['key']}_r.png").exists()}
    print(f"  R figures present: {len(r_keys)}/{len(FIGURES)}")

    # HTML lives one level up (examples/gallery-index.html) so paths work
    index = OUT.parent / "gallery-index.html"
    index.write_text(build_html(FIGURES, r_keys), encoding="utf-8")
    print(f"\n  gallery-index.html written ({index.stat().st_size // 1024} KB)")
    print(f"\nGallery ready:  open {index}")


if __name__ == "__main__":
    main()
