def _strip_data(n_per=50, seed=None):
    r = rng if seed is None else np.random.default_rng(seed)
    groups = np.repeat(["Neuron", "Astrocyte", "Microglia"], n_per)
    vals = np.concatenate([r.lognormal(2.5, 0.8, n_per),
                            r.lognormal(1.9, 0.9, n_per),
                            r.lognormal(2.2, 1.0, n_per)])
    return groups, vals


