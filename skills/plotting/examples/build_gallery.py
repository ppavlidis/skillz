def fig_strip_color(out):
    n_per = 30
    groups = np.tile(["Neuron", "Astrocyte", "Microglia"], n_per * 2)
    treat  = np.concatenate([np.tile(["ctrl", "kd"], n_per)] * 3)
    base   = np.concatenate([rng.lognormal(2.5, 0.8, n_per * 2),
                              rng.lognormal(1.9, 0.9, n_per * 2),
                              rng.lognormal(2.2, 1.0, n_per * 2)])
    vals = base * np.where(treat == "kd", 0.55, 1.0)
    _q(pavlab_stripchart, groups, vals, color=treat,
       color_label="Treatment",
       xlabel="Cell type", ylabel="TPM",
       log_y="log2", label_size="large", filename=str(out), figsize=SZ_STRIP)

