def _expr_mat():
    n_genes, n_samples = 12, 8
    m = pd.DataFrame(
        rng.standard_normal((n_genes, n_samples)),
        index=[f"Gene{i+1:02d}" for i in range(n_genes)],
        columns=[f"S{i+1}" for i in range(n_samples)],
    )
    m.iloc[:4, :4] += 2.5
    m.iloc[:4, 4:] -= 2.5
    return m


def fig_heatmap_expression(out):
    pavlab_heatmap(_expr_mat(), filename=str(out), figsize=SZ_H)

